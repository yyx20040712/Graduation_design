def wall_t(v_m3):
    """池壁厚度(m) -- 支持标量或 numpy 数组"""
    v = np.asarray(v_m3)
    return np.where(
        v < 500, 0.25, np.where(v < 2000, 0.30, np.where(v < 5000, 0.35, 0.40))
    )


def floor_t(v_m3):
    """底板厚度(m) -- 支持标量或 numpy 数组"""
    v = np.asarray(v_m3)
    return np.where(v < 1000, 0.30, np.where(v < 5000, 0.40, 0.50))


