# 五、单位处理规模造价指标 (2024年)
#    一级A标准: 3000~5000 元/(m3.d)
# ===============================================
UNIT_COST_RANGE = (3000, 5000)


def total_by_capacity(q_daily: float) -> Tuple[float, float, float]:
    """按处理规模估算(低, 高, 中) 万元"""
    lo = q_daily * UNIT_COST_RANGE[0] / 1e4
    hi = q_daily * UNIT_COST_RANGE[1] / 1e4
    return lo, hi, (lo + hi) / 2


def get_pipe_price(d: int) -> float:
    """管道单价, 线性插值 (与 pipe_network_cost._get_pipe_price 一致)"""
    if d in PIPE:
        return PIPE[d]
    keys = sorted(PIPE.keys())
    if d <= keys[0]:
        return PIPE[keys[0]]
    if d >= keys[-1]:
        return PIPE[keys[-1]]
    for i in range(len(keys) - 1):
        if keys[i] <= d <= keys[i + 1]:
            ratio = (d - keys[i]) / (keys[i + 1] - keys[i])
            return PIPE[keys[i]] + ratio * (PIPE[keys[i + 1]] - PIPE[keys[i]])
    return PIPE[keys[-1]]


